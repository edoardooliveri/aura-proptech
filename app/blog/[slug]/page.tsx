interface BlogPostPageProps {
  params: Promise<{ slug: string }>;
}

export default async function BlogPostPage({ params }: BlogPostPageProps) {
  const { slug } = await params;
  const article = articles[slug];
  if (!article) return notFound();

  // Simple markdown-like rendering
  const renderContent = (content: string) => {
    return content.split("\n\n").map((block, i) => {
      if (block.startsWith("## ")) {
        return (
          <h2
            key={i}
            className="font-mono text-xl md:text-2xl font-bold mt-10 mb-4"
            style={{ color: "var(--text-primary)" }}
          >
            {block.replace("## ", "")}
          </h2>
        );
      }
      return (
        <p
          key={i}
          className="text-sm md:text-base leading-relaxed mb-4"
          style={{ color: "var(--text-secondary)" }}
        >
          {block}
        </p>
      );
    });
  };

  return (
    <div className="min-h-screen bg-black text-white">
      <nav className="fixed top-0 left-0 right-0 z-50 flex items-center justify-between px-4 md:px-6 lg:px-12 h-20 bg-black border-b border-white/5">
        <Link href="/" className="flex items-center gap-2">
          <img src="/logo.svg" alt="Aura PropTech" className="h-14" />
        </Link>
        <Link href="/blog" className="text-xs font-mono hover:underline" style={{ color: "var(--text-muted)" }}>
          ← Tutti gli articoli
        </Link>
      </nav>

      <article className="pt-32 pb-20 px-4 md:px-6 max-w-3xl mx-auto">
        <div className="mb-8">
          <span
            className="text-[10px] font-mono font-bold px-2.5 py-1 rounded-full"
            style={{ background: "rgba(0,112,243,0.1)", color: "#0070F3", border: "1px solid rgba(0,112,243,0.2)" }}
          >
            {article.tag}
          </span>
          <div className="flex items-center gap-3 mt-4 text-xs font-mono" style={{ color: "var(--text-muted, #6B7280)" }}>
            <span>{article.date}</span>
            <span>·</span>
            <span>{article.readTime} di lettura</span>
          </div>
        </div>

        <h1
          className="font-mono text-3xl md:text-4xl lg:text-5xl font-bold mb-8 leading-tight"
          style={{ color: "var(--text-primary, #fff)" }}
        >
          {article.title}
        </h1>

        <div
          className="rounded-2xl p-6 md:p-10"
          style={{ background: "rgba(255,255,255,0.02)", border: "1px solid rgba(255,255,255,0.05)" }}
        >
          {renderContent(article.content)}
        </div>

        <div className="mt-12 text-center">
          <Link
            href="/blog"
            className="inline-flex items-center gap-2 text-sm font-mono font-bold hover:underline"
            style={{ color: "#0070F3" }}
          >
            ← Torna al blog
          </Link>
        </div>
      </article>
    </div>
  );
}
